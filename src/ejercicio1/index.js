// Se le da un inicio y un limite de vueltas al bucle con estas variables.
let vuelta = 1;
let limite = 17;

// Loop con while
while (vuelta <= limite) {
    console.log(`Vuelta numero ${vuelta}`);
    vuelta++;
};

// Loop con for
for(vuelta; vuelta <= limite; vuelta++){
    console.log(`Vueltita numero ${vuelta}`);
};